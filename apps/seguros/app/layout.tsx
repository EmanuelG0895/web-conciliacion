import "@repo/ui/styles.css";
import "./globals.css";
import type { Metadata } from "next";

import { Navbar, Profile, Sidebar, SidebarTrigger, ThemeProvider } from "@repo/ui";

import { cookies } from "next/headers";
import { Home, Image, Settings, Users } from "lucide-react";

import { SidebarProvider } from "@repo/providers";
import ThemeToggle from "@repo/ui/ui/src/theme/dark-mode-toggle/dark-mode-toggle";

export const metadata: Metadata = {
  title: "Create Turborepo",
  description: "Generated by create turbo",
};

export default async function RootLayout({
  children,
}: {
  readonly children: React.ReactNode;
}) {
  const cookieStore = cookies();

  const savedTheme = (await cookieStore).get("theme")?.value;

  const temaInicial = savedTheme === "dark" ? "dark" : "light";

  // En tu RootLayout.tsx o un archivo de configuración separado
  const sidebarLinks = [
    {
      label: "Inicio",
      href: "/",
      // Puedes usar cualquier icono de Lucide, asegúrate de importarlos arriba
      icon: <Home className="w-5 h-5" />,
    },
  ];
  return (
    <html lang="es">
      <body className="grid h-dvh grid-rows-[auto_1fr_auto]">
        <ThemeProvider initialTheme={temaInicial}>
          <SidebarProvider>
            <header>
              <Navbar logo={<SidebarTrigger />}>
                <div className="flex flex-col md:flex-row items-end md:justify-end">
                  <ThemeToggle />
                  <Profile
                    image_profile={<Image />}
                    image_alt="userImage"
                    userName="nombre de usuario"
                    showUserMenu={true}
                  />
                </div>
              </Navbar>
            </header>
            <main className="flex h-full overflow-hidden">
              <div className="flex overflow-y-auto">
                <Sidebar links={sidebarLinks} />
                {children}
              </div>
            </main>
            <footer>footer</footer>
          </SidebarProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
